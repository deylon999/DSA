capacities = [10, 14, 18]
names = ["Пети", "Васи", "Терентия"]


def knapsack(items, capacity):
    n = len(items)
    dp = [[0] * (capacity + 1) for _ in range(n + 1)]

    for i in range(1, n + 1):
        name, weight, value = items[i - 1]
        for w in range(capacity + 1):
            dp[i][w] = dp[i - 1][w]
            if weight <= w and dp[i - 1][w - weight] + value > dp[i][w]:
                dp[i][w] = dp[i - 1][w - weight] + value

    chosen = []
    w = capacity
    for i in range(n, 0, -1):
        if dp[i][w] != dp[i - 1][w]:
            chosen.append(i - 1)
            w -= items[i - 1][1]

    return dp[n][capacity], chosen

total = 0
used = set()

for k, capacity in enumerate(capacities):
    available = [(i, item) for i, item in enumerate(items) if i not in used]
    indexed_items = [item for _, item in available]
    original_indices = [i for i, _ in available]

    value, chosen = knapsack(indexed_items, capacity)
    total += value

    print(f"\nРюкзак {names[k]} (вместимость {capacity}):")
    print(f"  Стоимость: {value}")
    for idx in chosen:
        original_idx = original_indices[idx]
        used.add(original_idx)
        name, weight, val = items[original_idx]
        print(f"  - {name} (вес {weight}, стоимость {val})")

print(f"\nОбщая стоимость: {total}")